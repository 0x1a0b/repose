package org.openrepose.nodeservice.atomfeed;

/**
 * An enumeration of lifecycle events for Feed readers.
 */
public enum LifecycleEvents {
    // Indicates that a listener has successfully been registered, and may start to receive events
    LISTENER_REGISTERED,

    // Indicates that a listener has successfully been unregistered, and will no longer receive events
    LISTENER_UNREGISTERED,

    // Indicates that a feed reader has successfully been created based on the service configuration
    FEED_CREATED,

    // Indicates that a feed reader has successfully been activated, and is now reading from a live feed
    FEED_ACTIVATED,

    // Indicates that a feed reader has successfully been deactivated, and is no longer reading from a live feed
    FEED_DEACTIVATED,

    // Indicates that a feed reader has successfully been destroyed based on the service configuration
    FEED_DESTROYED
}
